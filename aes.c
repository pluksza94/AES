unsigned char GMul(unsigned char a, unsigned char b)
{
	unsigned char result=0;
	unsigned char hi_bit_set;
	int i;
	
	for(i=0;i<8;i++)
	{
		if((b&1)!=0)
		{
			result^=a;
		}
		
		hi_bit_set = a&0x80;
		a<<=1;
		
		if(hi_bit_set != 0)
		{
			a ^= 0x1b; // x^8 + x^4 + x^3 + x +1
		}
		b >>= 1;
	}
	return result;
}





// KEY SCHEDULE //
void RotWord(unsigned char array[])
{
	unsigned char tmp=array[0];
	
	array[0]=array[1];
	array[1]=array[2];
	array[2]=array[3];
	array[3]=tmp;
}

void SubWord(unsigned char array[])
{
	int i;
	for(i=0;i<4;i++)
	{
		array[i]=sbox[array[i]];
	}
}

void KeyExpansion(unsigned char roundKey[][Nb*(Nr+1)])
{
	unsigned char tmp[4];
	int i,j;
	
	for(j=Nk;j<Nb*(Nr+1);j++)
	{
		if(j%Nk==0)
		{
			for(i=0;i<4;i++)
			{
				tmp[i]=roundKey[i][j-1];
			}
			
			RotWord(tmp);
			SubWord(tmp);
			
			for(i=0;i<4;i++)
			{
				if(i==0)
				{
					roundKey[i][j]=tmp[i] ^ roundKey[i][j-Nk] ^ rcon[j/Nk];
				}
				
				else
				{
					roundKey[i][j]=tmp[i] ^ roundKey[i][j-Nk];
				}
			}
		}
		
		else
		{
			if(Nk==8 && j%Nk==4)
			{
				for(i=0;i<4;i++)
				{
					tmp[i]=roundKey[i][j-1];
				}
				
				SubWord(tmp);
				
				for(i=0;i<4;i++)
				{
					roundKey[i][j]=tmp[i] ^ roundKey[i][j-Nk];
				}
			}
			
			else
			{
				for(i=0;i<4;i++)
				{
					roundKey[i][j]=roundKey[i][j-Nk] ^ roundKey[i][j-1];
				}
			}
		}
	}
}





// ENCRYPTION PROCESS //
void SubBytes(unsigned char state[][Nb])
{
	int i,j;
	
	for(i=0;i<4;i++)
	{
		for(j=0;j<Nb;j++)
		{
			state[i][j]=sbox[state[i][j]];
		}
	}
}

void ShiftRows(unsigned char state[][Nb])
{
	unsigned char tmp[Nb];
	int i,j;
	
	for(i=0;i<4;i++)
	{
		for(j=0;j<Nb;j++)
		{
			tmp[j]=state[i][j];
		}
		
		for(j=0;j<Nb;j++)
		{
			state[i][j]=tmp[(j+i)%Nb];
		}
	}
}

void MixColumns(unsigned char state[][Nb])
{
	unsigned char tmp[Nb];
	int i, j, k;
	
	for(k=0;k<Nb;k++)
	{
		for(i=0;i<4;i++)
		{
			tmp[i]=0;
			
			for(j=0;j<Nb;j++)
			{
				tmp[i]=tmp[i]^GMul(mix[i][j],state[j][k]);
			}
		}
		
		for(i=0;i<Nb;i++)
		{
			state[i][k]=tmp[i];
		}
	}
}

void AddRoundKey(unsigned char state[][Nb], unsigned char roundKey[][Nb*(Nr+1)], int round)
{
	int i,j;
	
	for(i=0;i<4;i++)
	{
		for(j=0;j<Nb;j++)
		{
			state[i][j]=state[i][j] ^ roundKey[i][Nb*round + j];
		}
	}
}

void Cipher(unsigned char state[][Nb], unsigned char roundKey[][Nb*(Nr+1)])
{
	int i=0;
	
	AddRoundKey(state,roundKey,i);
	
	for(i=1;i<Nr;i++)
	{
		SubBytes(state);
		ShiftRows(state);
		MixColumns(state);
		AddRoundKey(state,roundKey,i);
	}
	
	SubBytes(state);
	ShiftRows(state);
	AddRoundKey(state,roundKey,Nr);
}





// DECRYPTION PROCESS //
void InvShiftRows(unsigned char state[][Nb])
{
	unsigned char tmp[Nb];
	int i,j;
	
	for(i=0;i<4;i++)
	{
		for(j=0;j<Nb;j++)
		{
			tmp[j]=state[i][j];
		}
		
		for(j=0;j<Nb;j++)
		{
			state[i][j]=tmp[(4+j-i)%Nb];
		}
	}
}

void InvSubBytes(unsigned char state[][Nb])
{
	int i,j;
	
	for(i=0;i<4;i++)
	{
		for(j=0;j<Nb;j++)
		{
			state[i][j]=inv_sbox[state[i][j]];
		}
	}
}

void InvMixColumns(unsigned char state[][Nb])
{
	unsigned char tmp[Nb];
	int i, j, k;
	
	for(k=0;k<Nb;k++)
	{
		for(i=0;i<4;i++)
		{
			tmp[i]=0;
			
			for(j=0;j<Nb;j++)
			{
				tmp[i]=tmp[i]^GMul(inv_mix[i][j],state[j][k]);
			}
		}
		
		for(i=0;i<Nb;i++)
		{
			state[i][k]=tmp[i];
		}
	}
}

void InvCipher(unsigned char state[][Nb], unsigned char roundKey[][Nb+(Nr+1)])
{
	int i=Nr;
	
	AddRoundKey(state,roundKey,i);
	
	for(i=Nr-1;i>0;i--)
	{
		InvShiftRows(state);
		InvSubBytes(state);
		AddRoundKey(state,roundKey,i);
		InvMixColumns(state);
	}
	
	InvShiftRows(state);
	InvSubBytes(state);
	AddRoundKey(state,roundKey,0);
}





